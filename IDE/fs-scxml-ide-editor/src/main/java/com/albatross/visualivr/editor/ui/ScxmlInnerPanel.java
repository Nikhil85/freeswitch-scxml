    private class TransistionTargetRenderer extends DefaultListCellRenderer {

        @Override
        public Component getListCellRendererComponent(JList list, Object value, int index, boolean isSelected, boolean cellHasFocus) {

            super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

            if (value != null && value instanceof TransitionTarget) {
                TransitionTarget target = (TransitionTarget) value;
                setText(target.getId());
            }

            return this;
        }
    }
