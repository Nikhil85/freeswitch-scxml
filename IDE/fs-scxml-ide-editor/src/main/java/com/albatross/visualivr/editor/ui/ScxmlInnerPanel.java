    public ScxmlInnerPanel(SectionView sectionView, SCXML scxml, ScxmlDataObject sdo) {
        super(sectionView);
        this.sdo = sdo;
        boxModel = new DefaultComboBoxModel(scxml.getTargets().values().toArray());
        boxModel.setSelectedItem(scxml.getInitialTarget());

        datamodel = scxml.getDatamodel();

        if (datamodel == null) {
            datamodel = new Datamodel();
            scxml.setDatamodel(datamodel);
        }

        this.dataTableModel = new DataTableModel(datamodel);
        this.dataTableModel.addTableModelListener(this);
        initComponents();
    }

